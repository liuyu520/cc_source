// ── 主分派 ──────────────────────────────────────────────────

/**
 * 所有 kind 的磁盘 primary 文件名(供 stale 清理用)
 *
 * 当同一 organism id 被再次 compile 但 kind 变了的时候,skillCompiler 需要
 * 把旧 kind 遗留的 primary 文件删掉 —— 这个表是"可能存在的残留名字"清单。
 *
 * ⚠️ 维护契约:当增加新的 GenomeKind(或让现有 kind 引入新的 primary 文件名)
 * 时,必须把对应的文件名追加到这里;否则 kind 变化再 compile 时,旧 body 会
 * 静默留存,目录里混合新旧产物。(SKILL.md "Stale-cleanup is name-whitelisted"
 * invariant 依赖这个表的完备性。)
 */
export const ALL_PRIMARY_FILENAMES: readonly string[] = [
  'SKILL.md',
  'PROMPT.md',
  'hook.sh',
  'hook.config.json',
]

/**
 * 按 kind 分派渲染。
 *   - 返回 primary (= CompileResult.bodyPath 指向的文件)
 *   - 返回 extras (仅 hook 会有:hook.config.json)
 *
 * kind='command' 与 'agent' 的 primary 文件名用 <name>.md —— 这与
 * ~/.claude/commands/ 和 ~/.claude/agents/ 的命名约定完全一致,reviewer
 * 复制文件时无需改名。
 */
export function renderBodyForKind(
  kind: GenomeKind,
  candidate: PatternCandidate,
): RenderedBody {
  const name = candidate.suggestedRemediation.nameSuggestion
  switch (kind) {
    case 'skill':
      return {
        primary: { fileName: 'SKILL.md', content: renderSkillBody(candidate) },
      }
    case 'command':
      return {
        primary: {
          fileName: `${name}.md`,
          content: renderCommandBody(candidate),
        },
      }
    case 'agent':
      return {
        primary: {
          fileName: `${name}.md`,
          content: renderAgentBody(candidate),
        },
      }
    case 'hook': {
      const { sh, config } = renderHookBody(candidate)
      return {
        primary: { fileName: 'hook.sh', content: sh },
        extras: [{ fileName: 'hook.config.json', content: config }],
      }
    }
    case 'prompt':
      return {
        primary: {
          fileName: 'PROMPT.md',
          content: renderPromptBody(candidate),
        },
      }
    default: {
      // 未来新增 kind 时编译期兜底:保留 SKILL.md 作为最安全的 fallback
      const exhaustive: never = kind
      void exhaustive
      return {
        primary: { fileName: 'SKILL.md', content: renderSkillBody(candidate) },
      }
    }
  }
}
