/**
 * prPlanWriter (2026-04-25, v1.0 §6.3)
 * ==========================================================================
 * "Shadow PR Plan"——不触发 git/gh 副作用,只把 "promote 成功后应该提的 PR"
 * 的元数据落到 ~/.claude/autoEvolve/pending-prs/<organismId>.md。
 *
 * 为什么不直接 git push + gh pr create
 * ─────────────────────────────────
 * spec §6.3 原文:"promote 成功后自动发 PR 到 main(不自动合 main)"。
 * 但本仓库是 restored Claude Code 源码树,push 的是用户的工作分支——
 * 对 shared state 的真副作用。遵循 CLAUDE.md + `feedback_real_validation`
 * 记忆条规则,只落本地 md,让 reviewer 拿去粘到 `gh pr create` 里。
 *
 * 接口
 * ────
 *   buildPrPlan(manifest, transition, opts?)  —— 纯函数,返回 PrPlan
 *   renderPrPlanMarkdown(plan)                —— 纯函数,返回 Markdown
 *   writePrPlan(plan)                         —— 副作用,写盘并返回 { ok, path }
 *
 * fail-open:任何异常都只打 debug log,不抛;`writePrPlan` 返回 ok=false,
 * 调用方(/evolve-accept)继续渲染 "PR plan failed, skipped" 提示即可。
 */
import { writeFileSync } from 'node:fs'
import type { OrganismManifest, Transition } from '../types.js'
import {
  ensureDir,
  getPendingPrDir,
  getPendingPrPath,
} from '../paths.js'
import { logForDebugging } from '../../../utils/debug.js'

/** PR Plan 数据结构 */
export interface PrPlan {
  /** organism id(用于落盘文件名) */
  organismId: string
  /** 建议 PR 标题 */
  title: string
  /** 建议 PR body(多行) */
  body: string
  /** PR base 分支(默认 main) */
  baseBranch: string
  /** PR head 分支(建议) —— 与 arena worktree 分支对齐 */
  headBranch: string
  /** 建议 reviewer 挂的 labels */
  labels: string[]
  /** 可选的 `gh pr create` 建议命令 */
  suggestedCmd: string
  /** 生成时间 ISO */
  generatedAt: string
}

/** 默认 base 分支。支持通过 env 覆盖以便测试/用户偏好。 */
function defaultBaseBranch(): string {
  return process.env.CLAUDE_EVOLVE_PR_BASE || 'main'
}

/** 建议 head 分支名。优先用 manifest 里的 worktreePath 尾巴(它与分支名约定一致)。 */
function deriveHeadBranch(manifest: OrganismManifest): string {
  // manifest.worktreePath 形如 .worktrees/<branchName>(见 arenaController)。
  // 若不可用,回退到 auto-evolve/<organism-id>。
  const wt = manifest.worktreePath ?? ''
  const m = wt.match(/\.worktrees\/(.+)$/)
  if (m && m[1]) return m[1]
  return `auto-evolve/${manifest.id}`
}

/**
 * 从 manifest + transition 构造 PR Plan。
 *
 * 不读任何外部文件,纯映射——便于在 /evolve-accept --dry-run 里也能"预览
 * 将会写什么"而不落盘。
 */
export function buildPrPlan(
  manifest: OrganismManifest,
  transition: Transition,
  opts?: {
    baseBranch?: string
    generatedAt?: string
  },
): PrPlan {
  const base = opts?.baseBranch || defaultBaseBranch()
  const head = deriveHeadBranch(manifest)
  const at = opts?.generatedAt || new Date().toISOString()

  const tierTag =
    transition.to === 'stable'
      ? '[stable]'
      : transition.to === 'canary'
        ? '[canary]'
        : `[${transition.to}]`

  const title = `autoEvolve ${tierTag} ${manifest.kind}:${manifest.name} (${manifest.id.slice(0, 12)})`

  // body 模板——刻意选 markdown 以便 gh pr create --body-file 直接吃;
  // 与 dailyDigest 同样的朴素结构:标题 + 原因 + 证据指针。
  const bodyLines: string[] = [
    `## Auto-Evolve Promotion: ${transition.from} → ${transition.to}`,
    '',
    `- **Organism**: \`${manifest.id}\` (${manifest.kind})`,
    `- **Name**: ${manifest.name}`,
    `- **Version**: ${manifest.version}`,
    `- **Parent**: \`${manifest.parent}\``,
    `- **Transition at**: ${transition.at}`,
    `- **Trigger**: ${transition.trigger}`,
    `- **Signature**: \`${transition.signature.slice(0, 16)}...\``,
    '',
    '### Rationale',
    transition.rationale || manifest.rationale || '(no rationale recorded)',
    '',
    '### Win Condition',
    manifest.winCondition
      ? '```\n' + manifest.winCondition + '\n```'
      : '(no win condition on file)',
    '',
    '### Fitness Snapshot',
    `- shadowTrials: ${manifest.fitness.shadowTrials}`,
    `- wins/losses/neutrals: ${manifest.fitness.wins}/${manifest.fitness.losses}/${manifest.fitness.neutrals}`,
    `- lastTrialAt: ${manifest.fitness.lastTrialAt ?? 'n/a'}`,
    '',
    '### Checklist (for reviewer)',
    '- [ ] Read `src/services/autoEvolve/arena/INDEX.md` for context',
    `- [ ] Verify manifest under \`~/.claude/autoEvolve/genome/${transition.to}/${manifest.id}/\``,
    '- [ ] Confirm transition signature via `/evolve-status`',
    transition.to === 'stable'
      ? '- [ ] Cross-check dailyDigest for any veto-window / Goodhart gate red flags today'
      : '- [ ] Check veto window (≥24h shadow→canary, ≥72h canary→stable)',
    '',
    '---',
    `_Generated by prPlanWriter at ${at}. This file is NOT a PR; nothing was pushed._`,
    '_To actually publish: \`gh pr create --base ' + base + ' --head ' + head + ' --title "..." --body-file <this-file>\`_',
  ]
  const body = bodyLines.join('\n')

  const suggestedCmd = [
    'gh pr create',
    `--base ${base}`,
    `--head ${head}`,
    `--title "${title.replace(/"/g, '\\"')}"`,
    `--body-file ~/.claude/autoEvolve/pending-prs/${manifest.id}.md`,
    '--label auto-evolve',
  ].join(' \\\n  ')

  return {
    organismId: manifest.id,
    title,
    body,
    baseBranch: base,
    headBranch: head,
    labels: ['auto-evolve', `tier:${transition.to}`, `kind:${manifest.kind}`],
    suggestedCmd,
    generatedAt: at,
  }
}

/**
 * 把 PR Plan 渲染为落盘 Markdown。前面加 YAML frontmatter 方便机械解析。
 */
export function renderPrPlanMarkdown(plan: PrPlan): string {
  const fm = [
    '---',
    `organismId: ${plan.organismId}`,
    `title: ${JSON.stringify(plan.title)}`,
    `baseBranch: ${plan.baseBranch}`,
    `headBranch: ${plan.headBranch}`,
    `labels: [${plan.labels.map(l => JSON.stringify(l)).join(', ')}]`,
    `generatedAt: ${plan.generatedAt}`,
    '---',
    '',
  ].join('\n')
  const cmdBlock = [
    '## Suggested `gh pr create` command',
    '',
    '```bash',
    plan.suggestedCmd,
    '```',
    '',
  ].join('\n')
  return fm + plan.body + '\n\n' + cmdBlock
}

/** 副作用:写 `~/.claude/autoEvolve/pending-prs/<id>.md`。fail-open。 */
export function writePrPlan(plan: PrPlan): {
  ok: boolean
  path: string | null
  error?: string
} {
  try {
    const dir = getPendingPrDir()
    ensureDir(dir)
    const path = getPendingPrPath(plan.organismId)
    writeFileSync(path, renderPrPlanMarkdown(plan), 'utf8')
    return { ok: true, path }
  } catch (e) {
    logForDebugging(
      `[prPlanWriter] writePrPlan failed: ${(e as Error).message}`,
    )
    return { ok: false, path: null, error: (e as Error).message }
  }
}
